     private void ManageTxtEditable(){
        txt1.setEnabled(true);
        txt3.setEnabled(true);
        switch(jcb.getSelectedIndex()){
                case 0:{
                     txt3.setEnabled(false);
                     txt3.setText("");
                     break;
                }
                case 1:{
                     txt1.setEnabled(false);
                     txt1.setText("");
                     break;
                }
                }
    }
