    private void mpg1BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mpg1BtnActionPerformed
        float bitInterval=0,bitRate=0;
        try{
            if(!"".equals(txt1.getText()))
            bitRate=Float.parseFloat(txt1.getText());
            else
            bitRate=0;
        }catch(NumberFormatException e){
            NumberError(l1.getText());

        }
        try{
            if(!"".equals(txt3.getText()))
            bitInterval=Float.parseFloat(txt3.getText());
            else
            bitInterval=0;
        }catch(NumberFormatException e){
            NumberError(l3.getText());
        }
        switch(jcb.getSelectedIndex()){
            case 0:{
                bitInterval=1/bitRate;
                txt3.setText(bitInterval+"");
                break;
            }
            case 1:{
                bitRate=1/bitInterval;
                txt1.setText(bitRate+"");
                break;
            }
        }
    }//GEN-LAST:event_mpg1BtnActionPerformed

