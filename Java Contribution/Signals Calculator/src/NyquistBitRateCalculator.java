    private void mpg1BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mpg1BtnActionPerformed
        float bitRate=0,bandwidth=0,L=0;
        try{
            if(!"".equals(txt1.getText()))
            bandwidth=Float.parseFloat(txt1.getText());
            else
            bandwidth=0;
        }catch(NumberFormatException e){
            NumberError(l1.getText());

        }
        try{
            if(!"".equals(txt2.getText()))
            L=Float.parseFloat(txt2.getText());
            else
            L=0;
        }catch(NumberFormatException e){
            NumberError(l2.getText());
        }
        try{
            if(!"".equals(txt3.getText()))
            bitRate=Float.parseFloat(txt3.getText());
            else
            bitRate=0;
        }catch(NumberFormatException e){
            NumberError(l3.getText());
        }
        switch(jcb.getSelectedIndex()){
            case 0:{
                bitRate= (float) (2 * bandwidth * (Math.log(L)/Math.log(2)));
                txt3.setText(bitRate+"");
                break;
            }
            case 1:{
                bandwidth= (float) (bitRate / (2 * (Math.log(L)/Math.log(2))));
                txt1.setText(bandwidth+"");
                break;
            }
            case 2:{
                L= (float) Math.pow(2,(bitRate / (2 * bandwidth)));
                txt2.setText(L+"");
                break;
            }
        }
    }//GEN-LAST:event_mpg1BtnActionPerformed

