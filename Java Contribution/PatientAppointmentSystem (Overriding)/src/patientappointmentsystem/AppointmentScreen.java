    private void loadTable()    
    {
        dtm.setRowCount(0);
        for (int i = 0; i < p.DoctorList.size(); i++) {

            Object[] objs = {p.DoctorList.get(i).getId(),p.DoctorList.get(i).getName(),
               p.DoctorList.get(i).getPhoneNumber(),
                         p.DoctorList.get(i).getEmail(),
                        p.DoctorList.get(i).getAge(),
//                        p.DoctorList.get(i).getPassword(),
                        p.DoctorList.get(i).getCNIC(),
                        p.DoctorList.get(i).getQualification(),
                        p.DoctorList.get(i).getSpecialization(),
                        p.DoctorList.get(i).getCheckedPatients()
                        };
            dtm.addRow(objs);
            
        }
    }

