    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Appointment a = new Appointment();
        boolean flag = false;
        for (Doctor doctor : p.DoctorList) {
//            System.out.print(doctorId.getText()+ "+" + (Integer.valueOf(doctor.getId())).toString());
            if(doctorId.getText().equals((Integer.valueOf(doctor.getId())).toString()))
            {
                a.setDoctorId(doctor.getId());
                a.setPatientId(pat.getId());
                p.setAppointment(a);
                JOptionPane.showMessageDialog(null, "Your Appointment is registered");
                flag = true;
                break;
            } 
        }
        if(!flag){
            JOptionPane.showMessageDialog(null, "Invalid Attempt");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

